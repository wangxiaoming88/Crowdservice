package com.crowdserviceinc.crowdservice.model;

import java.io.Serializable;

public class JobData implements Serializable {
	private JobBean job = null;
	private UserDetail user = null;
	private Category category = null;
	private BiddingDetail bidDetail = null;
	private String profileImgUrl = "";

	public String getProfileImgUrl() {
		return profileImgUrl;
	}

	public void setProfileImgUrl(String profileImgUrl) {
		this.profileImgUrl = profileImgUrl;
	}

	public BiddingDetail getBidDetail() {
		return bidDetail;
	}

	public void setBidDetail(BiddingDetail bidDetail) {
		this.bidDetail = bidDetail;
	}

	public JobBean getJob() {
		return job;
	}

	public void setJob(JobBean job) {
		this.job = job;
	}

	public UserDetail getUser() {
		return user;
	}

	public void setUser(UserDetail user) {
		this.user = user;
	}

	public Category getCategory() {
		return category;
	}

	public void setCategory(Category category) {
		this.category = category;
	}
}
